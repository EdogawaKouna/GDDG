    public static string GetIterationPrompt(string moduleName, string currentContent, string changeRequest, GameInput input)
    {
        var moduleNames = new Dictionary<string, string>
        {
            ["coreRules"] = "核心玩法规则",
            ["economy"] = "经济系统",
            ["combat"] = "战斗/数值框架",
            ["progression"] = "成长系统",
            ["monetization"] = "商业化方案",
            ["competitor"] = "竞品分析"
        };

        var moduleDisplay = moduleNames.ContainsKey(moduleName) ? moduleNames[moduleName] : moduleName;

        return $@"当前游戏设定：
{input}

当前【{moduleDisplay}】的现有内容：

{currentContent}

用户要求修改：{changeRequest}

请根据修改要求，重新生成完整的【{moduleDisplay}】章节内容（不要只输出修改部分，要输出修改后的完整内容）。
请使用纯文本格式，保持原有结构要求（包含数值和建议）。
在原内容基础上做针对性调整，不要完全抛弃已有内容。";
    }
